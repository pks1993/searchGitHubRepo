import Foundation
import UIKit
extension UIFont {
    
    class var robotoBoldFont: UIFont {
        return UIFont(name: "Roboto-Bold", size: 15.0)!
    }
    
    class var robotoRegularFont : UIFont {
        return UIFont(name: "Roboto-Regular", size: 13.0)!
    }
    
}
